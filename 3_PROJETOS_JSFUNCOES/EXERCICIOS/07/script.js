// escreva uma função que receba um número negativo e retorne um número positivo;
//Dica: utilize a função Math.abs;

function converterNumeroNegativo(neg) {
  return Math.abs(neg);
}

console.log(converterNumeroNegativo(-15));
console.log(converterNumeroNegativo(-25));
console.log(converterNumeroNegativo(10));
console.log(converterNumeroNegativo(-1));
