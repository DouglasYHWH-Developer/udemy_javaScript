// Escreva uma funçõ que receba dois números, o primeiro é a base e
// o segundo a potência;
// Depois faça operação e retorne o resultado;
// Por exemplo: 3, 2 = 9;

function calculaExpoente(base, potencia) {
  return Math.pow(base, potencia);
}

console.log(calculaExpoente(3, 3));
console.log(calculaExpoente(3, 2));
console.log(calculaExpoente(10, 3));